#ifndef _H_GUI_WND_
#define _H_GUI_WND_

////////////////////////////////////////////////////////////////////////////////
// FILE: gui_wnd.h
// DESC: Cointains the interface for the user interface. Draws the window based
//	 graphical user interface.
////////////////////////////////////////////////////////////////////////////////

#include <stdio.h>
#include <stdbool.h>
#include <stdarg.h>

#include "gui/gui_int.h"
#include "gui/logger.h"

////////////////////////////////////////////////////////////////////////////////
// Initializes the console.
////////////////////////////////////////////////////////////////////////////////
bool wnd_init();

////////////////////////////////////////////////////////////////////////////////
// Closes the console and releases used memory.
////////////////////////////////////////////////////////////////////////////////
void wnd_deinit();

////////////////////////////////////////////////////////////////////////////////
// Handles the user input.
////////////////////////////////////////////////////////////////////////////////
void wnd_run();

////////////////////////////////////////////////////////////////////////////////
// Sets the current status message
////////////////////////////////////////////////////////////////////////////////
void wnd_set_status(const char *msg, ...);

////////////////////////////////////////////////////////////////////////////////
// Logs the given formatted message
// PARA:
//	lvl	- the log level
//	msg	- the log message
//	...	- formatted parameter
////////////////////////////////////////////////////////////////////////////////
void wnd_log(int lvl, const char *msg, ...);	

////////////////////////////////////////////////////////////////////////////////
// Logs the given formatted message and takes a va_list as parameter.
// PARA:
//	lvl	- the log level
//	msg	- the log message
//	ap	- formatted parameter
////////////////////////////////////////////////////////////////////////////////
void wnd_log_v(int lvl, const char *msg, va_list ap);

#endif // _H_GUI_WND_
